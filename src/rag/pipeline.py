"""
RAG Pipeline for Healthcare Medical Q&A — v2 (upgraded).

Improvements over v1:
  1. Medical-domain embedding model (PubMedBERT) — better retrieval precision
  2. CrossEncoder reranker — re-scores FAISS candidates, top inject_k fed to LLM
  3. Improved prompt — instructs LLM to stay close to research conclusions
  4. retrieve() uses top_k=10 candidates → reranker selects best 3 for LLM
  5. BM25 hybrid retrieval supported when rank-bm25 is installed

Public API (stable):
    build_rag_pipeline(**kwargs) -> RAGPipeline
    answer(query, pipeline=None, **kwargs) -> dict
    retrieve(query, pipeline=None, **kwargs) -> list[dict]

    class RAGPipeline:
        retrieve(query, top_k=None) -> list[dict]
        retrieve_by_category(query, category, top_k=None) -> list[dict]
        generate(query, retrieved_chunks) -> str
        answer(query, top_k=None) -> dict
        answer_with_routing(query, category=None, top_k=None) -> dict
"""

import os
import pickle
import re
import threading
from pathlib import Path

try:
    from tenacity import retry, stop_after_attempt, wait_exponential, retry_if_exception_type
    HAS_TENACITY = True
except ImportError:
    HAS_TENACITY = False


# ── Constants ─────────────────────────────────────────────────────────────────

DISCLAIMER = (
    "\n\n⚠️ MEDICAL DISCLAIMER: This response is generated by an AI system "
    "for educational and informational purposes only. It is NOT a substitute "
    "for professional medical advice, diagnosis, or treatment. Always consult "
    "a qualified healthcare provider for medical decisions."
)

INSUFFICIENT_CONTEXT_MESSAGE = (
    "The retrieved medical literature does not contain enough information "
    "to answer this question confidently. Please consult the listed sources "
    "directly or rephrase your question."
)

# Upgraded: PubMedBERT fine-tuned on biomedical retrieval
DEFAULT_EMBEDDING_MODEL = "pritamdeka/S-PubMedBert-MS-MARCO"
DEFAULT_LLM_MODEL       = "google/flan-t5-base"
DEFAULT_RERANKER_MODEL  = "cross-encoder/ms-marco-MiniLM-L-6-v2"

DEFAULT_TOP_K             = 10    # retrieve more; reranker selects best
DEFAULT_INJECT_K          = 3     # chunks actually fed to LLM after reranking
DEFAULT_MAX_CONTEXT_WORDS = 200
DEFAULT_MAX_NEW_TOKENS    = 256
DEFAULT_MIN_ANSWER_WORDS  = 3

PROJECT_ROOT       = Path(__file__).resolve().parent.parent.parent
FAISS_INDEX_PATH   = PROJECT_ROOT / "data" / "embeddings" / "faiss_index" / "pubmedqa_index_flatl2.faiss"
CHUNK_MAPPING_PATH = PROJECT_ROOT / "data" / "embeddings" / "faiss_index" / "chunk_mapping.pkl"


# ── Helpers ───────────────────────────────────────────────────────────────────

_SOURCE_MARKER_RE = re.compile(r'\s*\[\s*sources?\s*\d+\s*\]\s*', re.IGNORECASE)
_MULTISPACE_RE    = re.compile(r'\s+')
_LEADING_PUNCT_RE = re.compile(r'^[\W_]+')


def _truncate_words(text: str, max_words: int) -> str:
    if not text:
        return ""
    words = text.split()
    if len(words) <= max_words:
        return text
    return " ".join(words[:max_words]) + "..."


def _clean_answer(text: str) -> str:
    if not text:
        return ""
    text = _SOURCE_MARKER_RE.sub(" ", text)
    text = _MULTISPACE_RE.sub(" ", text).strip()
    text = _LEADING_PUNCT_RE.sub("", text).strip()
    return text


def _is_insufficient(answer: str, min_words: int) -> bool:
    if not answer:
        return True
    word_count = sum(1 for tok in answer.split() if any(c.isalpha() for c in tok))
    return word_count < min_words


# ── RAG Pipeline ──────────────────────────────────────────────────────────────

class RAGPipeline:
    """
    Retrieval-Augmented Generation pipeline — v2.

    Components:
      1. SentenceTransformer (PubMedBERT) — domain-specific query embedding
      2. FAISS index — vector retrieval (top-10 candidates)
      3. BM25 keyword index (optional hybrid retrieval)
      4. CrossEncoder reranker — re-scores candidates, selects best inject_k
      5. Groq LLM (llama-3.1-8b-instant) or flan-t5-base fallback
    """

    def __init__(
        self,
        embedding_model: str    = DEFAULT_EMBEDDING_MODEL,
        llm_model: str          = DEFAULT_LLM_MODEL,
        reranker_model: str     = DEFAULT_RERANKER_MODEL,
        use_reranker: bool      = True,
        top_k: int              = DEFAULT_TOP_K,
        inject_k: int           = DEFAULT_INJECT_K,
        max_context_words: int  = DEFAULT_MAX_CONTEXT_WORDS,
        max_new_tokens: int     = DEFAULT_MAX_NEW_TOKENS,
        min_answer_words: int   = DEFAULT_MIN_ANSWER_WORDS,
        faiss_index_path: str   = None,
        chunk_mapping_path: str = None,
    ):
        import faiss
        import numpy as np
        from sentence_transformers import SentenceTransformer
        self._np    = np
        self._faiss = faiss

        self.top_k             = top_k
        self.inject_k          = inject_k
        self.max_context_words = max_context_words
        self.min_answer_words  = min_answer_words
        self.max_new_tokens    = max_new_tokens

        # ── Embedding model (PubMedBERT) ──────────────────────────────
        print(f"Loading embedding model: {embedding_model}")
        self.encoder = SentenceTransformer(embedding_model)

        # ── FAISS index ───────────────────────────────────────────────
        idx_path = faiss_index_path or str(FAISS_INDEX_PATH)
        print(f"Loading FAISS index: {idx_path}")
        self.index = faiss.read_index(idx_path)
        print(f"  Vectors in index: {self.index.ntotal:,}")

        # ── Chunk mapping ─────────────────────────────────────────────
        map_path = chunk_mapping_path or str(CHUNK_MAPPING_PATH)
        print(f"Loading chunk mapping: {map_path}")
        with open(map_path, "rb") as f:
            self.mapping_df = pickle.load(f)

        # ── BM25 (optional) ───────────────────────────────────────────
        try:
            from src.rag.bm25_retriever import BM25Retriever
            self.bm25      = BM25Retriever(self.mapping_df)
            self._use_bm25 = True
        except ImportError:
            self._use_bm25 = False

        try:
            from config.settings import settings
            self._bm25_threshold = settings.BM25_THRESHOLD
        except Exception:
            self._bm25_threshold = 5.0

        # ── CrossEncoder Reranker ─────────────────────────────────────
        self._use_reranker = use_reranker
        if use_reranker:
            try:
                from sentence_transformers import CrossEncoder
                print(f"Loading reranker: {reranker_model}")
                self.reranker = CrossEncoder(reranker_model)
                print("✅ Reranker ready")
            except Exception as e:
                print(f"⚠️  Reranker unavailable ({e}) — skipping reranking")
                self._use_reranker = False

        # ── LLM ───────────────────────────────────────────────────────
        groq_key = os.getenv("GROQ_API_KEY", "")
        if groq_key:
            from openai import OpenAI
            print(f"Loading LLM via Groq API: {llm_model}")
            self._groq_client = OpenAI(
                api_key=groq_key,
                base_url="https://api.groq.com/openai/v1",
            )
            self._groq_model = llm_model
            self._use_groq   = True
            print("✅ Groq client ready")
        else:
            print("GROQ_API_KEY not set — falling back to local flan-t5-base")

            from transformers import AutoTokenizer, AutoModelForSeq2SeqLM
            import torch

            model_name = "google/flan-t5-base"

            self.tokenizer = AutoTokenizer.from_pretrained(model_name)

            self.model = AutoModelForSeq2SeqLM.from_pretrained(
                model_name
            )

            self.max_new_tokens = max_new_tokens

            self._torch = torch

            self._use_groq = False

        print("✅ RAG Pipeline ready")

    # ── Retrieval ─────────────────────────────────────────────────────────────

    def _row_to_dict(self, idx: int, dist: float) -> dict:
        row = self.mapping_df.iloc[idx]
        return {
            "chunk_id":   idx,
            "question":   row["question"],
            "context":    row["context"],
            "answer":     row["answer"],
            "category":   row.get("category", "Unknown"),
            "text_chunk": row["text_chunk"],
            "distance":   dist,
        }

    def _rerank(self, query: str, candidates: list) -> list:
        """Re-score candidates with CrossEncoder and return sorted list."""
        if not self._use_reranker or not candidates:
            return candidates

        pairs = [
            [query, c.get("answer", "") + " " + _truncate_words(c.get("context", ""), 100)]
            for c in candidates
        ]
        scores = self.reranker.predict(pairs)

        for c, score in zip(candidates, scores):
            c["reranker_score"] = float(score)

        return sorted(candidates, key=lambda x: x.get("reranker_score", 0), reverse=True)

    def retrieve(self, query: str, top_k: int = None) -> list:
        """
        Hybrid retrieval: BM25 + FAISS → CrossEncoder reranker.

        Steps:
          1. FAISS retrieves top_k semantic candidates
          2. BM25 prepends high-confidence keyword hits (if available)
          3. CrossEncoder reranks the merged pool
        """
        k = top_k or self.top_k

        query_vector = self.encoder.encode(
            [query], convert_to_numpy=True
        ).astype(self._np.float32)
        D, faiss_idx = self.index.search(query_vector, k)
        faiss_results = [
            self._row_to_dict(int(faiss_idx[0, r]), float(D[0, r]))
            for r in range(k)
        ]

        if not self._use_bm25:
            candidates = faiss_results
        else:
            bm25_results = self.bm25.retrieve(query, top_k=k)
            seen, merged = set(), []
            for r in bm25_results:
                if r["bm25_score"] > self._bm25_threshold and r["chunk_id"] not in seen:
                    merged.append(r)
                    seen.add(r["chunk_id"])
            for r in faiss_results:
                if r["chunk_id"] not in seen:
                    merged.append(r)
                    seen.add(r["chunk_id"])
            candidates = merged[:k]

        return self._rerank(query, candidates)

    def retrieve_by_category(self, query: str, category: str, top_k: int = None) -> list:
        """Retrieve top-k chunks prioritising `category`, then rerank."""
        k = top_k or self.top_k
        search_k = min(k * 3, self.index.ntotal)

        query_vector = self.encoder.encode(
            [query], convert_to_numpy=True
        ).astype(self._np.float32)
        D, faiss_idx = self.index.search(query_vector, search_k)

        candidates = [
            self._row_to_dict(int(faiss_idx[0, r]), float(D[0, r]))
            for r in range(search_k)
        ]

        matched   = [c for c in candidates if c["category"] == category]
        unmatched = [c for c in candidates if c["category"] != category]
        pool      = (matched + unmatched)[:k]

        return self._rerank(query, pool)

    # ── Generation ────────────────────────────────────────────────────────────

    def _build_prompt(self, query: str, chunks: list) -> str:
        """
        Build the LLM prompt from the top inject_k reranked chunks.

        Research Conclusion (answer field) placed first — LLM instructed
        to stay close to the research language, keeping answers grounded.
        """
        evidence_blocks = []
        for i, chunk in enumerate(chunks[: self.inject_k]):
            answer_text = chunk.get("answer", "").strip()
            ctx = _truncate_words(chunk["context"], self.max_context_words)
            evidence_blocks.append(
                f"Research Conclusion {i + 1}: {answer_text}\n"
                f"Supporting Context: {ctx}"
            )
        evidence = "\n---\n".join(evidence_blocks)

        return (
            "You are a medical research assistant. "
            "Using ONLY the research conclusions below, write a concise, accurate answer "
            "to the medical question. "
            "Use the same medical terminology as the research conclusions. "
            "Do not add any information not found in the evidence.\n\n"
            f"Medical Evidence:\n{evidence}\n\n"
            f"Question: {query}\n\n"
            "Concise Answer (based strictly on the research conclusions above):"
        )

    def _call_groq(self, prompt: str) -> str:
        _system = (
            "You are a medical research assistant. Answer medical questions "
            "by closely following the provided research conclusions. "
            "Be concise, accurate, and use the same terminology as the evidence. "
            "Never add information not present in the provided evidence."
        )

        def _do_call():
            response = self._groq_client.chat.completions.create(
                model=self._groq_model,
                messages=[
                    {"role": "system", "content": _system},
                    {"role": "user",   "content": prompt},
                ],
                max_tokens=self.max_new_tokens,
                temperature=0.1,
            )
            return response.choices[0].message.content.strip()

        if HAS_TENACITY:
            @retry(
                retry=retry_if_exception_type(Exception),
                wait=wait_exponential(multiplier=1, min=2, max=30),
                stop=stop_after_attempt(3),
                reraise=True,
            )
            def _retried():
                return _do_call()
            return _retried()
        else:
            return _do_call()

    def generate(self, query: str, retrieved_chunks: list) -> str:
        """Generate a cleaned answer using the top reranked chunks."""
        if not retrieved_chunks:
            return INSUFFICIENT_CONTEXT_MESSAGE

        prompt = self._build_prompt(query, retrieved_chunks)

        if self._use_groq:
            raw = self._call_groq(prompt)
        else:
            inputs = self.tokenizer(
                prompt,
                return_tensors="pt",
                truncation=True,
                max_length=1024,
            )

            with self._torch.no_grad():
                outputs = self.model.generate(
                    **inputs,
                    max_new_tokens=self.max_new_tokens,
                    num_beams=4,
                    early_stopping=True,
                    no_repeat_ngram_size=3,
                    do_sample=False,
                )

            raw = self.tokenizer.decode(
                outputs[0],
                skip_special_tokens=True
            )

        cleaned = _clean_answer(raw)

        if cleaned.lower().startswith("insufficient evidence"):
            return INSUFFICIENT_CONTEXT_MESSAGE
        if _is_insufficient(cleaned, self.min_answer_words):
            return INSUFFICIENT_CONTEXT_MESSAGE

        return cleaned

    def generate_extractive(self, query: str, retrieved_chunks: list) -> str:
        """
        Kept for backwards compatibility.
        Returns answer fields directly without LLM paraphrasing.
        NOTE: For evaluation use generate() — the extractive approach does not
        outperform abstractive generation on ROUGE-L due to semantic mismatch
        in the retrieval pool.
        """
        if not retrieved_chunks:
            return INSUFFICIENT_CONTEXT_MESSAGE

        answers = []
        for chunk in retrieved_chunks[: self.inject_k]:
            ans = chunk.get("answer", "").strip()
            if ans and len(ans.split()) >= self.min_answer_words:
                answers.append(ans)

        if not answers:
            return INSUFFICIENT_CONTEXT_MESSAGE

        return _clean_answer(" ".join(answers[:2]))

    # ── Public answer methods ─────────────────────────────────────────────────

    def _format_sources(self, retrieved: list) -> list:
        results = []
        for r in retrieved:
            raw_dist = r["distance"]
            if raw_dist <= 0:
                relevance = min(1.0, -raw_dist / 30.0)
            else:
                relevance = max(0.0, 1.0 - raw_dist / 200.0)
            results.append({
                "chunk_id":        r["chunk_id"],
                "question":        r["question"],
                "category":        r["category"],
                "distance":        round(raw_dist, 4),
                "relevance_score": round(relevance, 4),
                "reranker_score":  round(r.get("reranker_score", 0.0), 4),
                "excerpt":         r.get("context", "")[:150].strip(),
            })
        return results

    def answer(self, query: str, top_k: int = None) -> dict:
        """Full RAG pipeline: retrieve → rerank → generate → disclaimer."""
        retrieved  = self.retrieve(query, top_k)
        raw_answer = self.generate(query, retrieved)

        return {
            "question":          query,
            "answer":            raw_answer + DISCLAIMER,
            "answer_raw":        raw_answer,
            "retrieved_sources": self._format_sources(retrieved),
            "disclaimer_present": True,
            "top_k":             len(retrieved),
        }

    def answer_with_routing(self, query: str, category: str = None, top_k: int = None) -> dict:
        """Full pipeline with optional classifier routing + reranking."""
        if category:
            retrieved = self.retrieve_by_category(query, category, top_k)
        else:
            retrieved = self.retrieve(query, top_k)

        raw_answer = self.generate(query, retrieved)
        sources    = self._format_sources(retrieved)

        return {
            "question":          query,
            "category":          category or "Unknown",
            "answer":            raw_answer + DISCLAIMER,
            "answer_raw":        raw_answer,
            "retrieved_sources": sources,
            "disclaimer_present": True,
            "top_k":             len(retrieved),
            "category_matched_sources": sum(
                1 for s in sources if s["category"] == category
            ),
        }


# ── Module-level convenience (cached singleton) ────────────────────────────────

_pipeline_instance = None
_pipeline_lock     = threading.Lock()


def build_rag_pipeline(**kwargs) -> RAGPipeline:
    """Build and cache a RAG pipeline instance (thread-safe singleton)."""
    global _pipeline_instance
    if _pipeline_instance is None:
        with _pipeline_lock:
            if _pipeline_instance is None:
                try:
                    from config.settings import settings
                    defaults = {
                        "llm_model":       settings.LLM_MODEL,
                        "embedding_model": settings.EMBEDDING_MODEL,
                        "reranker_model":  settings.RERANKER_MODEL,
                        "use_reranker":    settings.USE_RERANKER,
                        "top_k":           settings.TOP_K,
                        "inject_k":        settings.INJECT_K,
                        "max_new_tokens":  settings.MAX_TOKENS,
                    }
                    defaults.update(kwargs)
                    kwargs = defaults
                except Exception:
                    pass
                _pipeline_instance = RAGPipeline(**kwargs)
    return _pipeline_instance


def answer(query: str, pipeline: RAGPipeline = None, **kwargs) -> dict:
    global _pipeline_instance
    if pipeline is None:
        if _pipeline_instance is None:
            _pipeline_instance = build_rag_pipeline()
        pipeline = _pipeline_instance
    return pipeline.answer(query, **kwargs)


def retrieve(query: str, pipeline: RAGPipeline = None, **kwargs) -> list:
    global _pipeline_instance
    if pipeline is None:
        if _pipeline_instance is None:
            _pipeline_instance = build_rag_pipeline()
        pipeline = _pipeline_instance
    return pipeline.retrieve(query, **kwargs)
