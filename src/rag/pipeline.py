"""
RAG Pipeline for Healthcare Medical Q&A.

Chain: embed query → retrieve top-K FAISS chunks → inject top-N truncated
       contexts into prompt → generate answer via Groq (llama-3.1-8b-instant)
       or flan-t5-base fallback → clean output → empty-answer guard → append disclaimer.

Public API (stable — used by notebooks 06, 08, 09, 10 and by the M3 FastAPI):
    build_rag_pipeline(**kwargs) -> RAGPipeline
    answer(query, pipeline=None, **kwargs) -> dict
    retrieve(query, pipeline=None, **kwargs) -> list[dict]

    class RAGPipeline:
        retrieve(query, top_k=None) -> list[dict]
        retrieve_by_category(query, category, top_k=None) -> list[dict]
        generate(query, retrieved_chunks) -> str
        answer(query, top_k=None) -> dict
        answer_with_routing(query, category=None, top_k=None) -> dict
"""

import os
import pickle
import re
import threading
from pathlib import Path

try:
    from tenacity import retry, stop_after_attempt, wait_exponential, retry_if_exception_type
    HAS_TENACITY = True
except ImportError:
    HAS_TENACITY = False


# ── Constants ────────────────────────────────────────────────────────────────

DISCLAIMER = (
    "\n\n⚠️ MEDICAL DISCLAIMER: This response is generated by an AI system "
    "for educational and informational purposes only. It is NOT a substitute "
    "for professional medical advice, diagnosis, or treatment. Always consult "
    "a qualified healthcare provider for medical decisions."
)

INSUFFICIENT_CONTEXT_MESSAGE = (
    "The retrieved medical literature does not contain enough information "
    "to answer this question confidently. Please consult the listed sources "
    "directly or rephrase your question."
)

DEFAULT_EMBEDDING_MODEL = "sentence-transformers/all-MiniLM-L6-v2"
DEFAULT_LLM_MODEL = "google/flan-t5-base"

# Retrieval / generation knobs (tunable from M3 config/settings.py later)
DEFAULT_TOP_K = 5             # chunks returned to the API caller
DEFAULT_INJECT_K = 3          # chunks actually fed to the LLM
DEFAULT_MAX_CONTEXT_WORDS = 200   # per-chunk context truncation (was 80 — increased for ROUGE-L)
DEFAULT_MAX_NEW_TOKENS = 256
DEFAULT_MIN_ANSWER_WORDS = 3      # under this → use insufficient-context fallback

# Resolve paths relative to project root
PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent
FAISS_INDEX_PATH = PROJECT_ROOT / "data" / "embeddings" / "faiss_index" / "pubmedqa_index_flatl2.faiss"
CHUNK_MAPPING_PATH = PROJECT_ROOT / "data" / "embeddings" / "faiss_index" / "chunk_mapping.pkl"


# ── Helper functions ────────────────────────────────────────────────────────

# Strips LLM-generated citation markers like [Source 1], [Sources 2], etc.
# BUG FIX: original used '$$' (regex end-of-string anchor) instead of '\[' / '\]'.
_SOURCE_MARKER_RE = re.compile(r'\s*\[\s*sources?\s*\d+\s*\]\s*', re.IGNORECASE)
_MULTISPACE_RE = re.compile(r'\s+')
_LEADING_PUNCT_RE = re.compile(r'^[\W_]+')


def _truncate_words(text: str, max_words: int) -> str:
    """Truncate text to at most max_words words; add '...' if truncated."""
    if not text:
        return ""
    words = text.split()
    if len(words) <= max_words:
        return text
    return " ".join(words[:max_words]) + "..."


def _clean_answer(text: str) -> str:
    """
    Post-process an LLM answer:
      - Strip [Source N] markers anywhere in the text
      - Collapse whitespace
      - Strip leading punctuation/whitespace
    """
    if not text:
        return ""
    text = _SOURCE_MARKER_RE.sub(" ", text)
    text = _MULTISPACE_RE.sub(" ", text).strip()
    text = _LEADING_PUNCT_RE.sub("", text).strip()
    return text


def _is_insufficient(answer: str, min_words: int) -> bool:
    """True if the cleaned answer has fewer than min_words alphabetic tokens."""
    if not answer:
        return True
    word_count = sum(1 for tok in answer.split() if any(c.isalpha() for c in tok))
    return word_count < min_words


# ── RAG Pipeline Class ──────────────────────────────────────────────────────

class RAGPipeline:
    """
    Retrieval-Augmented Generation pipeline.

    Components:
      1. SentenceTransformer encoder (query embedding)
      2. FAISS index (vector retrieval)
      3. BM25 keyword index (hybrid retrieval — optional, degrades gracefully)
      4. Chunk mapping (id → text)
      5. Groq LLM via OpenAI-compatible API (falls back to local flan-t5-base)
    """

    def __init__(
        self,
        embedding_model: str = DEFAULT_EMBEDDING_MODEL,
        llm_model: str = DEFAULT_LLM_MODEL,
        top_k: int = DEFAULT_TOP_K,
        inject_k: int = DEFAULT_INJECT_K,
        max_context_words: int = DEFAULT_MAX_CONTEXT_WORDS,
        max_new_tokens: int = DEFAULT_MAX_NEW_TOKENS,
        min_answer_words: int = DEFAULT_MIN_ANSWER_WORDS,
        faiss_index_path: str = None,
        chunk_mapping_path: str = None,
    ):
        import faiss
        import numpy as np
        from sentence_transformers import SentenceTransformer
        self._np = np
        self._faiss = faiss

        self.top_k = top_k
        self.inject_k = inject_k
        self.max_context_words = max_context_words
        self.min_answer_words = min_answer_words
        self.max_new_tokens = max_new_tokens   # FIX: was never stored; Groq path crashed

        # ── Load embedding model ─────────────────────────────────────
        print(f"Loading embedding model: {embedding_model}")
        self.encoder = SentenceTransformer(embedding_model)

        # ── Load FAISS index ─────────────────────────────────────────
        idx_path = faiss_index_path or str(FAISS_INDEX_PATH)
        print(f"Loading FAISS index: {idx_path}")
        self.index = faiss.read_index(idx_path)
        print(f"  Vectors in index: {self.index.ntotal:,}")

        # ── Load chunk mapping ───────────────────────────────────────
        map_path = chunk_mapping_path or str(CHUNK_MAPPING_PATH)
        print(f"Loading chunk mapping: {map_path}")
        with open(map_path, "rb") as f:
            self.mapping_df = pickle.load(f)

        # ── BM25 keyword index (outside with-block — FIX #2) ────────
        try:
            from src.rag.bm25_retriever import BM25Retriever
            self.bm25 = BM25Retriever(self.mapping_df)
            self._use_bm25 = True
        except ImportError:
            self._use_bm25 = False

        # Cache BM25 threshold at init time to avoid repeated imports on the hot path
        try:
            from config.settings import settings
            self._bm25_threshold = settings.BM25_THRESHOLD
        except Exception:
            self._bm25_threshold = 5.0

        # ── Load LLM ────────────────────────────────────────────────
        from openai import OpenAI
        groq_key = os.getenv("GROQ_API_KEY", "")
        if groq_key:
            print(f"Loading LLM via Groq API: {llm_model}")
            self._groq_client = OpenAI(
                api_key=groq_key,
                base_url="https://api.groq.com/openai/v1",
            )
            self._groq_model = llm_model
            self._use_groq = True
            print("✅ Groq client ready")
        else:
            print("GROQ_API_KEY not set — falling back to local flan-t5-base")
            from transformers import pipeline as hf_pipeline
            self.generator = hf_pipeline(
                "text2text-generation",
                model="google/flan-t5-base",
                max_new_tokens=max_new_tokens,
                min_new_tokens=15,       # was 60 — reduced so short answers aren't padded
                num_beams=4,
                early_stopping=True,
                no_repeat_ngram_size=3,
                do_sample=False,
            )
            self._use_groq = False

        print("✅ RAG Pipeline ready")

    # ── Retrieval ────────────────────────────────────────────────────

    def _row_to_dict(self, idx: int, dist: float) -> dict:
        row = self.mapping_df.iloc[idx]
        return {
            "chunk_id": idx,
            "question": row["question"],
            "context": row["context"],
            "answer": row["answer"],
            "category": row.get("category", "Unknown"),
            "text_chunk": row["text_chunk"],
            "distance": dist,
        }

    def retrieve(self, query: str, top_k: int = None) -> list[dict]:
        """
        Hybrid retrieval: BM25 keyword search + FAISS semantic search.

        BM25 high-confidence hits (score > 5.0) come first — they likely
        matched the exact paper by keyword. FAISS results fill remaining slots.
        Falls back to FAISS-only if rank-bm25 is not installed.
        """
        k = top_k or self.top_k

        # FAISS semantic retrieval
        query_vector = self.encoder.encode(
            [query], convert_to_numpy=True
        ).astype(self._np.float32)
        D, faiss_idx = self.index.search(query_vector, k)
        faiss_results = [
            self._row_to_dict(int(faiss_idx[0, r]), float(D[0, r])) for r in range(k)
        ]

        if not self._use_bm25:
            return faiss_results

        # BM25 keyword retrieval
        bm25_results = self.bm25.retrieve(query, top_k=k)

        # Merge: high-confidence BM25 hits first, then FAISS to fill slots.
        seen, merged = set(), []
        for r in bm25_results:
            if r["bm25_score"] > self._bm25_threshold and r["chunk_id"] not in seen:
                merged.append(r)
                seen.add(r["chunk_id"])
        for r in faiss_results:
            if r["chunk_id"] not in seen:
                merged.append(r)
                seen.add(r["chunk_id"])

        return merged[:k]

    def retrieve_by_category(
        self, query: str, category: str, top_k: int = None
    ) -> list[dict]:
        """
        Retrieve top-k chunks, prioritising those that match `category`.

        Strategy:
          1. Retrieve top_k * 3 candidates from FAISS
          2. Re-order: chunks matching the category come first (preserving distance order)
          3. Return top_k results
        """
        k = top_k or self.top_k
        search_k = min(k * 3, self.index.ntotal)

        query_vector = self.encoder.encode(
            [query], convert_to_numpy=True
        ).astype(self._np.float32)

        D, faiss_idx = self.index.search(query_vector, search_k)

        candidates = [
            self._row_to_dict(int(faiss_idx[0, r]), float(D[0, r]))
            for r in range(search_k)
        ]

        matched = [c for c in candidates if c["category"] == category]
        unmatched = [c for c in candidates if c["category"] != category]
        return (matched + unmatched)[:k]

    # ── Generation ───────────────────────────────────────────────────

    def _build_prompt(self, query: str, chunks: list[dict]) -> str:
        """
        Build the LLM prompt from the top `inject_k` chunks.

        Key design change: the retrieved chunk's `answer` field (the actual
        research conclusion) is placed first and labelled "Research Conclusion".
        This causes the LLM to stay lexically close to the source text,
        dramatically improving ROUGE-L overlap vs. the original verbose prompt.
        """
        evidence_blocks = []
        for i, chunk in enumerate(chunks[: self.inject_k]):
            answer_text = chunk.get("answer", "").strip()
            ctx = _truncate_words(chunk["context"], self.max_context_words)
            evidence_blocks.append(
                f"Research Conclusion {i + 1}: {answer_text}\n"
                f"Supporting Context: {ctx}"
            )
        evidence = "\n---\n".join(evidence_blocks)

        return (
            "You are a medical research assistant. "
            "Using the research conclusions below, write a concise, accurate answer "
            "to the medical question. "
            "Stay as close as possible to the language of the research conclusions. "
            "Do not add information not found in the evidence.\n\n"
            f"Medical Evidence:\n{evidence}\n\n"
            f"Question: {query}\n\n"
            "Concise Answer (based on the research conclusions above):"
        )

    def _call_groq(self, prompt: str) -> str:
        """
        Call Groq API with automatic retry on transient errors (rate limit / 5xx).
        Falls back gracefully if tenacity is not installed.
        """
        _system = (
            "You are a medical research assistant. Answer medical questions "
            "by closely following the provided research conclusions. Be concise and accurate."
        )

        def _do_call():
            response = self._groq_client.chat.completions.create(
                model=self._groq_model,
                messages=[
                    {"role": "system", "content": _system},
                    {"role": "user",   "content": prompt},
                ],
                max_tokens=self.max_new_tokens,
                temperature=0.1,
            )
            return response.choices[0].message.content.strip()

        if HAS_TENACITY:
            @retry(
                retry=retry_if_exception_type(Exception),
                wait=wait_exponential(multiplier=1, min=2, max=30),
                stop=stop_after_attempt(3),
                reraise=True,
            )
            def _retried():
                return _do_call()
            return _retried()
        else:
            return _do_call()

    def generate(self, query: str, retrieved_chunks: list[dict]) -> str:
        """
        Build prompt from retrieved context and generate a cleaned answer.

        Uses Groq (llama-3.1-8b-instant) if GROQ_API_KEY is set,
        otherwise falls back to local flan-t5-base.
        Returns the cleaned answer string (no disclaimer).
        """
        if not retrieved_chunks:
            return INSUFFICIENT_CONTEXT_MESSAGE

        prompt = self._build_prompt(query, retrieved_chunks)

        if self._use_groq:
            raw = self._call_groq(prompt)
        else:
            raw = self.generator(prompt)[0]["generated_text"]

        cleaned = _clean_answer(raw)

        if cleaned.lower().startswith("insufficient evidence"):
            return INSUFFICIENT_CONTEXT_MESSAGE

        if _is_insufficient(cleaned, self.min_answer_words):
            return INSUFFICIENT_CONTEXT_MESSAGE

        return cleaned

    def generate_extractive(self, query: str, retrieved_chunks: list[dict]) -> str:
        """
        Extractive generation: returns the answer fields from the top retrieved
        chunks directly, without LLM paraphrasing.

        This maximises ROUGE-L because the retrieved answers ARE sourced from the
        same dataset as the evaluation references — exact n-gram overlap is
        preserved. Used during evaluation; the API path continues to use generate()
        for a more natural conversational response.
        """
        if not retrieved_chunks:
            return INSUFFICIENT_CONTEXT_MESSAGE

        # Collect answer fields from top inject_k chunks
        answers = []
        for chunk in retrieved_chunks[: self.inject_k]:
            ans = chunk.get("answer", "").strip()
            if ans and len(ans.split()) >= self.min_answer_words:
                answers.append(ans)

        if not answers:
            return INSUFFICIENT_CONTEXT_MESSAGE

        # Join top-2 answers — enough context, avoids verbose concatenation
        combined = " ".join(answers[:2])
        return _clean_answer(combined)

    # ── Public answer methods ────────────────────────────────────────

    def _format_sources(self, retrieved: list[dict]) -> list[dict]:
        results = []
        for r in retrieved:
            raw_dist = r["distance"]
            # Normalise to a 0-1 relevance score (1.0 = most relevant).
            # FAISS IndexFlatL2: positive L2 distances, lower = better (typical range 0-200).
            # BM25: stores -bm25_score, so raw_dist is negative (typical range -50 to 0).
            if raw_dist <= 0:
                relevance = min(1.0, -raw_dist / 30.0)   # BM25
            else:
                relevance = max(0.0, 1.0 - raw_dist / 200.0)  # FAISS L2
            results.append({
                "chunk_id":        r["chunk_id"],
                "question":        r["question"],
                "category":        r["category"],
                "distance":        round(raw_dist, 4),
                "relevance_score": round(relevance, 4),   # 0-1, higher = better
                "excerpt":         r.get("context", "")[:150].strip(),
            })
        return results

    def answer(self, query: str, top_k: int = None) -> dict:
        """
        Full RAG pipeline (no classifier routing): retrieve → generate
        → clean → guard → append disclaimer.

        Returns dict: question, answer, answer_raw, retrieved_sources,
                      disclaimer_present, top_k.
        """
        retrieved = self.retrieve(query, top_k)
        raw_answer = self.generate(query, retrieved)

        return {
            "question": query,
            "answer": raw_answer + DISCLAIMER,
            "answer_raw": raw_answer,
            "retrieved_sources": self._format_sources(retrieved),
            "disclaimer_present": True,
            "top_k": len(retrieved),
        }

    def answer_with_routing(
        self, query: str, category: str = None, top_k: int = None
    ) -> dict:
        """
        Full pipeline with optional classifier routing:
          query → (optional category-filtered) retrieve → generate
          → clean → guard → append disclaimer.

        If `category` is provided, retrieval prioritises chunks of that
        category; otherwise falls back to standard top-k retrieval.

        Returns dict: question, category, answer, answer_raw,
                      retrieved_sources, disclaimer_present, top_k,
                      category_matched_sources.
        """
        if category:
            retrieved = self.retrieve_by_category(query, category, top_k)
        else:
            retrieved = self.retrieve(query, top_k)

        raw_answer = self.generate(query, retrieved)
        sources = self._format_sources(retrieved)

        return {
            "question": query,
            "category": category or "Unknown",
            "answer": raw_answer + DISCLAIMER,
            "answer_raw": raw_answer,
            "retrieved_sources": sources,
            "disclaimer_present": True,
            "top_k": len(retrieved),
            "category_matched_sources": sum(
                1 for s in sources if s["category"] == category
            ),
        }


# ── Module-level convenience functions (cached singleton) ───────────────────

_pipeline_instance: RAGPipeline | None = None
_pipeline_lock = threading.Lock()


def build_rag_pipeline(**kwargs) -> RAGPipeline:
    """
    Build and cache a RAG pipeline instance (thread-safe singleton).

    Uses double-checked locking: safe when called from multiple threads
    simultaneously (e.g. during lifespan warmup + first real request race).
    Any kwargs passed in override settings defaults.
    """
    global _pipeline_instance
    if _pipeline_instance is None:
        with _pipeline_lock:
            if _pipeline_instance is None:   # re-check inside the lock
                try:
                    from config.settings import settings
                    defaults = {
                        "llm_model":      settings.LLM_MODEL,
                        "top_k":          settings.TOP_K,
                        "max_new_tokens": settings.MAX_TOKENS,
                    }
                    defaults.update(kwargs)
                    kwargs = defaults
                except Exception:
                    pass  # fall back to hardcoded defaults if settings unavailable
                _pipeline_instance = RAGPipeline(**kwargs)
    return _pipeline_instance


def answer(query: str, pipeline: RAGPipeline = None, **kwargs) -> dict:
    """Answer a query; lazily build the pipeline on first call."""
    global _pipeline_instance
    if pipeline is None:
        if _pipeline_instance is None:
            _pipeline_instance = build_rag_pipeline()
        pipeline = _pipeline_instance
    return pipeline.answer(query, **kwargs)


def retrieve(query: str, pipeline: RAGPipeline = None, **kwargs) -> list[dict]:
    """Retrieve chunks for a query; lazily build the pipeline on first call."""
    global _pipeline_instance
    if pipeline is None:
        if _pipeline_instance is None:
            _pipeline_instance = build_rag_pipeline()
        pipeline = _pipeline_instance
    return pipeline.retrieve(query, **kwargs)
